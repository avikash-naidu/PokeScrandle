/** Returns the PokéAPI CDN sprite URL for a given Pokédex ID */
export function spriteUrl(id: number): string {
  return `https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/${id}.png`
}

/** Pick a random Pokémon, optionally excluding one (to avoid VS-ing itself) */
export function randomPokemon(exclude: Pokemon | null = null): Pokemon {
  let pick: Pokemon
  do {
    pick = POKEMON[Math.floor(Math.random() * POKEMON.length)]
  } while (pick === exclude)
  return pick
}
